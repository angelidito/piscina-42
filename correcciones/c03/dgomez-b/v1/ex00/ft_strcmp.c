/* ******************************* PROTOTIPOS ******************************* */

int	ft_strcmp(char *s1, char *s2);

/* ******************************* FUNCIONES ******************************** */

int	ft_strcmp(char *s1, char *s2)
{
	int	j;

	j = 0;
	if (*(s1 + j) < *(s2 + j))
		return (-1);
	else if (*(s1 + j) > *(s2 + j))
		return (1);
	while (*(s1 + j) && *(s2 + j) != '\0')
	{
		j++;
		if (*(s1 + j) < *(s2 + j))
			return (-1);
		else if (*(s1 + j) > *(s2 + j))
			return (1);
	}
	return (0);
}
