char	*ft_strstr(char *str, char *to_find);

/* ******************************* FUNCIONES ******************************** */

char	*ft_strstr(char *str, char *to_find)
{
	unsigned int	i;
	unsigned int	j;

	i = 0;
	j = 0;
	while (*(str + i) && *(to_find + j) != '\0')
	{
		if (*(str + i) == *(to_find + j))
			j++;
		else
			j = 0;
		i++;
	}
	if (*(to_find + j) != '\0')
		return (0x0);
	return (str + i - j);
}
