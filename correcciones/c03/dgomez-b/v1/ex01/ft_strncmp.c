/* ******************************* PROTOTIPOS ******************************* */

int	ft_strncmp(char *s1, char *s2, unsigned int n);

/* ******************************* FUNCIONES ******************************** */

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	j;

	j = 0;
	if (n > 0)
	{
		if (*(s1 + j) < *(s2 + j))
			return (-1);
		else if (*(s1 + j) > *(s2 + j))
			return (1);
		while ((*(s1 + j) && *(s2 + j) != '\0') && (j < n - 1))
		{
			j++;
			if (*(s1 + j) < *(s2 + j))
				return (-1);
			else if (*(s1 + j) > *(s2 + j))
				return (1);
		}
	}
	return (0);
}
